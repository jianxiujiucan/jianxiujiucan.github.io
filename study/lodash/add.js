/**
 * 两个数相加。
 * @param {number} a 相加的第一个数
 * @param {number} b 相加的第二个数
 * @returns {number} 返回总和
 * @example
 * 
 * add(6, 4);
 * // => 10
 */

function add(a, b) {
  return a + b
}

